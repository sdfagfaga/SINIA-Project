package com.sinnia.utils;

public class AllProducts  {

    private String productName;
    private String productCost;
    private String productNumbers;
    private String productType;
    private String productMinQty;

    public String getProductName() {
        return productName;
    }

    public void setProductName(String productName) {
        this.productName = productName;
    }

    public String getProductCost() {
        return productCost;
    }

    public void setProductCost(String productCost) {
        this.productCost = productCost;
    }

    public String getProductNumbers() {
        return productNumbers;
    }

    public void setProductNumbers(String productNumbers) {
        this.productNumbers = productNumbers;
    }

    public String getProductType() {
        return productType;
    }

    public void setProductType(String productType) {
        this.productType = productType;
    }

    public String getProductMinQty() {
        return productMinQty;
    }

    public void setProductMinQty(String productMinQty) {
        this.productMinQty = productMinQty;
    }
}
