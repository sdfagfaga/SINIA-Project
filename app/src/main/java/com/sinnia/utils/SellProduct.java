package com.sinnia.utils;

public class SellProduct {

    private String sellProductName;
    private String sellBoxPrice;
    private String numOfBoxs;
    private String quantity;
    private String totalPrice;

    public String getSellProductName() {
        return sellProductName;
    }

    public void setSellProductName(String sellProductName) {
        this.sellProductName = sellProductName;
    }

    public String getSellBoxPrice() {
        return sellBoxPrice;
    }

    public void setSellBoxPrice(String sellBoxPrice) {
        this.sellBoxPrice = sellBoxPrice;
    }

    public String getNumOfBoxs() {
        return numOfBoxs;
    }

    public void setNumOfBoxs(String numOfBoxs) {
        this.numOfBoxs = numOfBoxs;
    }

    public String getQuantity() {
        return quantity;
    }

    public void setQuantity(String quantity) {
        this.quantity = quantity;
    }

    public String getTotalPrice() {
        return totalPrice;
    }

    public void setTotalPrice(String totalPrice) {
        this.totalPrice = totalPrice;
    }
}
